package firstPacakage;

public class Inheritance {
	int x = 45;

	Inheritance() {
		System.out.println("Inside Inheritance Constructor");
	}

	Inheritance(float arg) {
		System.out.println("Inside Inheritance Constructor " + arg);
	}

	public static void m2() {
		System.out.println("m2 from inheritance");

	}

	public void m3() {
		System.out.println("Inside m3 of Inheritance ");
	}
}
